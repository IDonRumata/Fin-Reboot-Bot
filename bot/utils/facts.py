def _pick_item(items: list[str], user_id: int, context: str = "") -> str:
    """
    Deterministically pick an item from the list using hash(user_id + date + context).
    Same user gets the same item on the same calendar day; different day = different item.
    """
    today = date.today().isoformat()
    seed = f"{user_id}:{today}:{context}"
    digest = int(hashlib.md5(seed.encode()).hexdigest(), 16)
    return items[digest % len(items)]


def get_daily_fact(user_id: int) -> str:
    """Return a financial fact for today (unique per user per day)."""
    return _pick_item(FINANCIAL_FACTS, user_id, context="fact")


def get_daily_quote(user_id: int) -> str:
    """Return a motivational quote for today (unique per user per day)."""
    return _pick_item(MOTIVATIONAL_QUOTES, user_id, context="quote")


def get_fact_and_quote(user_id: int) -> str:
    """Return a combined block: fact + quote, both unique per user per day."""
    fact = get_daily_fact(user_id)
    quote = get_daily_quote(user_id)
    return f"{fact}\n\n{quote}"
